, PT_TRANSACTIONS AS (
    SELECT 
        HSP_TRANSACTIONS.TX_ID,
        'HB' as PB_HB,
        HSP_TRANSACTIONS.TX_POST_DATE,
        HSP_TRANSACTIONS.SERVICE_DATE,
        HSP_TRANSACTIONS.HSP_ACCOUNT_ID,
        HSP_TRANSACTIONS.PAT_ENC_CSN_ID,
        HSP_TRANSACTIONS.DEPARTMENT,
        HSP_TRANSACTIONS.COST_CNTR_ID,
        'HB-' || CL_COST_CNTR.COST_CNTR_ID || ' - ' || CL_COST_CNTR.COST_CENTER_NAME as COST_CENTER, 
        HSP_TRANSACTIONS.PROC_ID,
        CLARITY_EAP.PROC_CODE,
        HSP_TRANSACTIONS.CPT_CODE,
        HSP_TRANSACTIONS.PROCEDURE_DESC,
        HSP_TRANSACTIONS.BILLING_PROV_ID,
        HSP_TRANSACTIONS.PERFORMING_PROV_ID,
        HSP_TRANSACTIONS.QUANTITY,
        HSP_TRANSACTIONS.RVU,
        CUSTOM_PRODUCTIVITY_UNITS.CUSTOM_PRODUCTIVITY_WEIGHTS,
        HSP_TRANSACTIONS.QUANTITY * CUSTOM_PRODUCTIVITY_UNITS.CUSTOM_PRODUCTIVITY_WEIGHTS AS CUSTOM_TOTAL_UNITS
    FROM HSP_TRANSACTIONS
        LEFT JOIN CL_COST_CNTR ON HSP_TRANSACTIONS.COST_CNTR_ID = CL_COST_CNTR.COST_CNTR_ID
        LEFT JOIN CLARITY_EAP ON HSP_TRANSACTIONS.PROC_ID = CLARITY_EAP.PROC_ID
        LEFT JOIN CUSTOM_PRODUCTIVITY_UNITS ON CLARITY_EAP.PROC_CODE = CUSTOM_PRODUCTIVITY_UNITS.PROC_CODE
    WHERE TX_TYPE_HA_C = 1
        AND CL_COST_CNTR.COST_CENTER_CODE IN ('1380', '1780', '2600', '2610', '2810', '2860', '4800', '4801', '4810', '4811', '4815', '5060', '5061', '5065', '5080', '5081', '7280', '7290')
        
    UNION ALL
    
    SELECT 
        ARPB_TRANSACTIONS.TX_ID,
        'PB' AS PB_HB,
        ARPB_TRANSACTIONS.POST_DATE,
        ARPB_TRANSACTIONS.SERVICE_DATE,
        ARPB_TX_MODERATE.HOSP_ACCT_ID,
        ARPB_TRANSACTIONS.PAT_ENC_CSN_ID,
        ARPB_TRANSACTIONS.DEPARTMENT_ID,
        ARPB_TRANSACTIONS.BILL_AREA_ID,
        'PB-' || ARPB_TRANSACTIONS.BILL_AREA_ID || ' - ' || BILL_AREA.RECORD_NAME as COST_CENTER,
        ARPB_TRANSACTIONS.PROC_ID,
        CLARITY_EAP.PROC_CODE,
        ARPB_TRANSACTIONS.CPT_CODE,
        CLARITY_EAP.PROC_NAME,
        
        ARPB_TRANSACTIONS.BILLING_PROV_ID,
        ARPB_TRANSACTIONS.SERV_PROVIDER_ID,
        ARPB_TRANSACTIONS.PROCEDURE_QUANTITY,
        ARPB_TRANSACTIONS.RVU_TOTAL,
        CUSTOM_PRODUCTIVITY_UNITS.CUSTOM_PRODUCTIVITY_WEIGHTS,
        ARPB_TRANSACTIONS.PROCEDURE_QUANTITY * CUSTOM_PRODUCTIVITY_UNITS.CUSTOM_PRODUCTIVITY_WEIGHTS AS CUSTOM_TOTAL_UNITS

    FROM ARPB_TRANSACTIONS
    LEFT JOIN ARPB_TX_MODERATE ON ARPB_TRANSACTIONS.TX_ID = ARPB_TX_MODERATE.TX_ID
    LEFT JOIN ARPB_TRANSACTIONS2 ON ARPB_TRANSACTIONS.TX_ID = ARPB_TRANSACTIONS2.TX_ID
    LEFT JOIN BILL_AREA ON ARPB_TRANSACTIONS.BILL_AREA_ID = BILL_AREA.BILL_AREA_ID
    LEFT JOIN CLARITY_EAP ON ARPB_TRANSACTIONS.PROC_ID = CLARITY_EAP.PROC_ID
    LEFT JOIN CUSTOM_PRODUCTIVITY_UNITS ON CLARITY_EAP.PROC_CODE = CUSTOM_PRODUCTIVITY_UNITS.PROC_CODE
    WHERE TX_TYPE_C = 1
    AND ARPB_TRANSACTIONS.BILL_AREA_ID IN ('147', '384', '398', '413', '442' ,'446', '95')
)
, WORK_UNIT AS (
    SELECT 
        PT_TRANSACTIONS.COST_CENTER,
        PT_TRANSACTIONS.DEPARTMENT,
        PERFORMING_PROV.PROV_ID,
        PERFORMING_PROV.PROV_NAME,
        SUM(PT_TRANSACTIONS.QUANTITY) AS UNIT_OF_SERVICE,
        SUM(PT_TRANSACTIONS.RVU) AS DEFAULT_RVU,
        SUM(PT_TRANSACTIONS.CUSTOM_TOTAL_UNITS) AS CUSTOM_PRODUCTIVITY
    FROM PT_TRANSACTIONS
    LEFT JOIN CLARITY_SER PERFORMING_PROV ON PT_TRANSACTIONS.PERFORMING_PROV_ID = PERFORMING_PROV.PROV_ID
    WHERE PT_TRANSACTIONS.SERVICE_DATE BETWEEN EPIC_UTIL.EFN_DIN('{?Start Date}') AND EPIC_UTIL.EFN_DIN('{?End Date}')
    GROUP BY PT_TRANSACTIONS.COST_CENTER, DEPARTMENT, PERFORMING_PROV.PROV_ID, PERFORMING_PROV.PROV_NAME
)
, PT_APPTS AS (
    SELECT 
        DEPARTMENT_ID,
        DEPARTMENT_NAME, 
        PROV_ID, 
        PROV_NAME,
        SUM(CASE WHEN VISIT_CLASS = 'NPV' THEN 1 ELSE 0 END) AS NUM_NPV,
        SUM(CASE WHEN VISIT_CLASS = 'Return' THEN 1 ELSE 0 END) AS NUM_RETURN,
        COUNT(PAT_ENC_CSN_ID) AS NUM_VISITS
    FROM (
        SELECT 
            F_SCHED_APPT.PAT_ENC_CSN_ID,
            F_SCHED_APPT.DEPARTMENT_ID,
            CLARITY_DEP.DEPARTMENT_NAME,
            F_SCHED_APPT.PROV_ID,
            CLARITY_SER.PROV_NAME,
            F_SCHED_APPT.PRC_ID,
            CASE WHEN PRC_ID IN (1184, 1211, 1185, 1067, 1593, 17109, 1210, 1003, 1186, 1069)
                    THEN 'NPV'
                WHEN PRC_ID IN (717, 1171, 1212, 1068, 1066, 1205, 1206, 1207, 16556, 1594,
                                177, 501, 502, 114, 210, 503)
                    THEN 'Return'
            END AS VISIT_CLASS
        FROM F_SCHED_APPT
        LEFT JOIN CLARITY_DEP ON F_SCHED_APPT.DEPARTMENT_ID = CLARITY_DEP.DEPARTMENT_ID
        LEFT JOIN CLARITY_SER ON F_SCHED_APPT.PROV_ID = CLARITY_SER.PROV_ID
        WHERE F_SCHED_APPT.DEPARTMENT_ID IN (
            1015501001, 1009401011, 1000901011, 1000901004, 1005001024, 1005001030, 1005001031, 1005001029, 1005001025, 1005001022,
            1005001023, 1005001028, 1005001051, 1006401031, 1006401028, 1006401029, 1012201015, 1012201009, 1012301032, 1012301031,
            1012301033, 1012301035, 1014001003, 1028001068, 1028001069, 1028001070, 1028001104, 1028001105, 1019501003, 1019501002,
            1026301003, 1026301004, 1014800005, 1014800006, 1024201014, 1024201006, 1026401003, 1025601003, 1025601002, 1025601001,
            1024301067, 1024301068, 1024301031, 1024301030, 1024301033
        )
        AND F_SCHED_APPT.APPT_STATUS_C = '2'
        AND F_SCHED_APPT.APPT_DTTM BETWEEN EPIC_UTIL.EFN_DIN('{?Start Date}') AND EPIC_UTIL.EFN_DIN('{?End Date}')
        ORDER BY APPT_DTTM DESC
    ) GROUP BY DEPARTMENT_ID, DEPARTMENT_NAME, PROV_ID, PROV_NAME
   
)
SELECT
    WORK_UNIT.PROV_ID,
    WORK_UNIT.COST_CENTER as "Cost Center",
    CLARITY_DEP.DEPARTMENT_NAME as "Billing Department",
    PT_APPTS.DEPARTMENT_NAME as "Visit Department",
    WORK_UNIT.PROV_NAME as "Provider",
    WORK_UNIT.DEFAULT_RVU as "Default RVU",
    WORK_UNIT.CUSTOM_PRODUCTIVITY as "Custom Productivity",
    PT_APPTS.NUM_NPV as "NPV",
    PT_APPTS.NUM_RETURN as "Return",
    PT_APPTS.NUM_VISITS as "Total Visits"
FROM WORK_UNIT
    LEFT JOIN PT_APPTS ON WORK_UNIT.PROV_ID = PT_APPTS.PROV_ID 
    LEFT JOIN CLARITY_DEP ON WORK_UNIT.DEPARTMENT = CLARITY_DEP.DEPARTMENT_ID
WHERE 1=1
    -- If Outpatient Department, match on the Billing Department of the Charge (Cost Center)
    -- This seems to work but may have edge cases where this does not work
    AND (WORK_UNIT.DEPARTMENT = PT_APPTS.DEPARTMENT_ID OR PT_APPTS.DEPARTMENT_ID IS NULL) 
    --AND WORK_UNIT.PROV_ID = '33668'
ORDER BY WORK_UNIT.COST_CENTER, WORK_UNIT.PROV_NAME