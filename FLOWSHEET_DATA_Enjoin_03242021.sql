CSN_HAR AS
 (
    SELECT PRIM_ENC_CSN_ID,HSP_ACCOUNT_ID 
    FROM HSP_ACCOUNT HSP 
        INNER JOIN  PATIENT PAT ON HSP.PAT_ID = PAT.PAT_ID
        LEFT JOIN CLARITY.CLARITY_DEP DEP ON HSP.DISCH_DEPT_ID = DEP.DEPARTMENT_ID 
        LEFT JOIN CLARITY.ZC_PHYSICAL_LOC PLOC ON DEP.PHYSICAL_LOC_C = PLOC.PHYSICAL_LOC_C
        WHERE   HSP.DISCH_DATE_TIME >= SYSDATE - 30 AND HSP.DISCH_DATE_TIME <= SYSDATE - 1 AND TRUNC( MONTHS_BETWEEN( HSP.DISCH_DATE_TIME, PAT.BIRTH_DATE ) /12 )>=18
        AND HSP.CODING_STATUS_C = '4'AND  PLOC.PHYSICAL_LOC_C =1 
  )  
  

