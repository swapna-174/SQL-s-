WITH PHARMI
AS
(
SELECT
*
FROM
(
SELECT 
rec.PAT_ID  
,pat.PAT_NAME
,pat.PAT_MRN_ID
,pat.BIRTH_DATE
,ifm.MEAS_VALUE  
,ifm.RECORDED_TIME "RECORDED_TIME"
,to_char( ifm.RECORDED_TIME, 'HH24')  AS "HOUR"
,ifm.FSD_ID
,ifm.FLO_MEAS_ID
,ser.PROV_NAME    "PROVIDER_NAME"
,hsp.PAT_ENC_CSN_ID  "PATIENT_CSN"
,parloc.LOC_NAME  "PARENT_LOCATION"

FROM IP_FLWSHT_MEAS ifm
left outer join IP_FLWSHT_REC rec on ifm.FSD_ID = rec.FSD_ID
left outer join pat_enc hsp on rec.INPATIENT_DATA_ID = hsp.INPATIENT_DATA_ID
inner join patient pat on hsp.PAT_ID = pat.PAT_ID
LEFT OUTER JOIN CLARITY_SER ser ON ifm.TAKEN_USER_ID = ser.USER_ID
LEFT OUTER JOIN clarity_dep dep ON hsp.DEPARTMENT_ID = dep.DEPARTMENT_ID
LEFT OUTER JOIN clarity_loc loc ON dep.REV_LOC_ID = loc.LOC_ID
LEFT OUTER JOIN clarity_loc parloc ON loc.HOSP_PARENT_LOC_ID = parloc.LOC_ID



WHERE 
TRUNC(ifm.RECORDED_TIME) >= '1-may-2020'
--TRUNC(ifm.RECORDED_TIME) BETWEEN EPIC_UTIL.EFN_DIN('{?Start Date}') AND EPIC_UTIL.EFN_DIN('{?End Date}')
and ifm.FLO_MEAS_ID in (
'2100000100',
'2100000101',
'2100000600',
'2100000700',
'2100000900',
'2100001100',
'2100001200',
'2100001300',
'2100001400',
'2100001500',
'2100001600',
'2100001700',
'2100001800',
'2100001900',
'2100002000',
'2100002100',
'2100002200',
'2100002300',
'2100002400',
'2100002500',
'2100002600',
'2100002700',
'2100002800',
'2100002900',
'2100003000',
'2100003100',
'2100003200',
'2100003300',
'2100003400',
'2100003800',
'2100003900',
'2100003500'
)


)
pivot
  (
    max(MEAS_VALUE)  "VAL"
                FOR FLO_MEAS_ID in (
'2100000100'AS PHARM_FURTHER_OUTREACH ,
'2100000101' AS PHARM_INTERVENTION,
'2100000600' AS PHARM_INTERVENTION_YES,
'2100000700' AS PHARM_INTERVENTION_NO,
'2100000900' AS INTERVENTION_OUTREACH_PLAN,
'2100001100' AS FIRST_OUTREACH,
'2100001200' AS OUTREACH_NUMBER,
'2100001300' AS NUMBER_GAPS,
'2100001400' AS TIME_SPENT,
'2100001500' AS INTERVENTION_ADDT_NOTES,
'2100001600' AS ADHERENCE_MEASURES,
'2100001700' AS PDC_DIABETES,
'2100001800' AS PDC_HYPERTENSION,
'2100001900' AS PDC_CHOLESTEROL,
'2100002000' AS PDC_ASTHMA,
'2100002100' AS PDC_COPD,
'2100002200' AS PDC_BETA_BLOCKER,
'2100002300' AS MEDS_DIABETES,
'2100002400' AS MEDS_HYPERTENSION,
'2100002500' AS MEDS_CHOLESTEROL,
'2100002600' AS MEDS_ASTHMA,
'2100002700' AS MEDS_COPD,
'2100002800' AS MEDS_BETA_BLOCKER,
'2100002900' AS FILLED_DIABETES,
'2100003000' AS FILLED_HYPERTENSION,
'2100003100' AS FILLED_CHOLESTEROL,
'2100003200' AS FILLED_ASTHMA,
'2100003300' AS FILLED_COPD,
'2100003400' AS FILLED_BETA_BLOCKER,
'2100003800' AS PHARM_CONTACTED,
'2100003900' AS NUMBER_PHARM,
'2100003500' AS INITIAL_REVIEW_TIME_SPENT

)
  
 
)
)

,PATCVG
AS
(
    SELECT *
    FROM 
    (
    SELECT 
    pp.PAT_ID
    ,vcpp.BENEFIT_PLAN_NAME
    ,vcpp.PAYOR_NAME
    ,vcpp.COVERAGE_ID
    ,cml.MEM_EFF_FROM_DATE
    ,cml.MEM_EFF_TO_DATE
    ,pp.RECORDED_TIME
    ,pp.fsd_id
        ,row_number() OVER (PARTITION BY pp.PAT_ID,pp.RECORDED_TIME ORDER BY cml.MEM_EFF_FROM_DATE ) "SEQ_NUM"
    FROM  PHARMI pp
    INNER JOIN PAT_ACCT_CVG cvg ON pp.PAT_ID = cvg.PAT_ID AND cvg.ACCOUNT_TYPE_C = '1'
    INNER JOIN v_coverage_payor_plan vcpp ON cvg.COVERAGE_ID = vcpp.COVERAGE_ID
    INNER JOIN COVERAGE_MEMBER_LIST cml ON vcpp.COVERAGE_ID = cml.COVERAGE_ID
    WHERE
    ((TRUNC(pp.RECORDED_TIME) BETWEEN cml.MEM_EFF_FROM_DATE AND cml.MEM_EFF_TO_DATE) OR cml.MEM_EFF_TO_DATE IS NULL)
    )
    WHERE SEQ_NUM = 1
)    

SELECT DISTINCT
phrm.PAT_ID  
,phrm.PAT_NAME
,phrm.PAT_MRN_ID
,phrm.BIRTH_DATE
,pcvg.COVERAGE_ID
,pcvg.BENEFIT_PLAN_NAME
,pcvg.PAYOR_NAME
,phrm.RECORDED_TIME
,phrm.HOUR
,phrm.PROVIDER_NAME
,phrm.PATIENT_CSN
,phrm.PARENT_LOCATION
,phrm.PHARM_FURTHER_OUTREACH_VAL 
,phrm.PHARM_INTERVENTION_VAL
,phrm.PHARM_INTERVENTION_YES_VAL
,phrm.PHARM_INTERVENTION_NO_VAL
,phrm.INTERVENTION_OUTREACH_PLAN_VAL
,phrm.FIRST_OUTREACH_VAL
,phrm.OUTREACH_NUMBER_VAL
,phrm.NUMBER_GAPS_VAL
,phrm.TIME_SPENT_VAL
,phrm.INITIAL_REVIEW_TIME_SPENT_VAL
,phrm.PHARM_CONTACTED_VAL
,phrm.NUMBER_PHARM_VAL
,phrm.INTERVENTION_ADDT_NOTES_VAL
,phrm.ADHERENCE_MEASURES_VAL
,phrm.PDC_DIABETES_VAL
,phrm.PDC_HYPERTENSION_VAL
,phrm.PDC_CHOLESTEROL_VAL
,phrm.PDC_ASTHMA_VAL
,phrm.PDC_COPD_VAL
,phrm.PDC_BETA_BLOCKER_VAL
,phrm.MEDS_DIABETES_VAL
,phrm.MEDS_HYPERTENSION_VAL
,phrm.MEDS_CHOLESTEROL_VAL
,phrm.MEDS_ASTHMA_VAL
,phrm.MEDS_COPD_VAL
,phrm.MEDS_BETA_BLOCKER_VAL
,phrm.FILLED_DIABETES_VAL
,phrm.FILLED_HYPERTENSION_VAL
,phrm.FILLED_CHOLESTEROL_VAL
,phrm.FILLED_ASTHMA_VAL
,phrm.FILLED_COPD_VAL
,phrm.FILLED_BETA_BLOCKER_VAL
  
FROM PHARMI phrm
LEFT OUTER JOIN PATCVG pcvg ON phrm.PAT_ID = pcvg.PAT_ID AND phrm.RECORDED_TIME = pcvg.RECORDED_TIME
