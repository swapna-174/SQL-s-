SELECT
    COUNT(DISTINCT lda.IP_LDA_ID) "Procedure_Count"
    , EXTRACT(YEAR FROM hsp.HOSP_ADMSN_TIME) AS "Procedure_Year"
    , EXTRACT(MONTH FROM hsp.HOSP_ADMSN_TIME) AS "Procedure_Month"
    ,  'Flow Sheet Measures' AS "SOM_Module"
    ,  'Peripheral IV Placement' AS "Proc_Category" 
FROM
    CLARITY.PAT_ENC_HSP hsp
    INNER JOIN  CLARITY.IP_FLOWSHEET_ROWS ip ON hsp.INPATIENT_DATA_ID = ip.INPATIENT_DATA_ID
    INNER JOIN CLARITY.IP_LDA_NOADDSINGLE lda ON ip.IP_LDA_ID = lda.IP_LDA_ID AND ip.FLO_MEAS_ID = lda.FLO_MEAS_ID
WHERE
  	NOT ip.FLOWSHT_ROW_NAME LIKE '%REMOVED%'
  	AND NOT lda.DESCRIPTION LIKE '%REMOVED%'
    AND hsp.HOSP_ADMSN_TIME >= ADD_MONTHS(SYSDATE, -12)
    AND lda.DESCRIPTION LIKE '%Peripheral IV%'
    AND (ip.FLOWSHT_ROW_NAME LIKE '%Hand%'
        OR ip.FLOWSHT_ROW_NAME LIKE '% arm%'
        OR ip.FLOWSHT_ROW_NAME LIKE '%Wrist%')
    AND hsp.DEPARTMENT_ID IN ('1000100010', '1000108001', '1000107003', '1000107006', '1000106015', '1000108005', '1000105013', '1000106011', '1000100009', '1000105008'
            , '1000102028', '1000105012', '1000103038', '1000103034', '1000103036', '1000108002', '1000103046', '1000106016', '1000107005', '1000108006', '1000105011', '1000105002'
            , '1000103037', '1000102026', '1000105003', '1000108013', '1000108009', '1000105007', '1000108008', '1000103033', '1000105010', '1000105005', '1000108004', '1000107007'
            , '1000105006', '1000105004', '1000108007', '1000102031', '000107008', '1000108014', '1000102025', '1000105009', '1000106020', '1000106014', '1000105014', '1000102030'
            , '1000102022', '1000108012', '1000106017', '1000103035', '1000108003', '1000102029', '1000106013', '1000102023', '1000106012', '1000105015', '1000102027')
GROUP BY 
    EXTRACT(YEAR FROM hsp.HOSP_ADMSN_TIME)
    , EXTRACT(MONTH FROM hsp.HOSP_ADMSN_TIME)