--                                ('C70.0','C70.9','C71.0','C71.1','C71.2','C71.3','C71.4','C71.5','C71.6','C71.7',
--                        'C71.8','C71.9','C72.20','C72.21','C72.22','C72.30','C72.31','C72.32','C72.40','C72.41',
--                        'C72.42','C72.50','C72.59','C75.1','C75.2','C75.3','C79.31','C79.32','D32.0','D32.9',
--                        'D33.0','D33.1','D33.2','D33.3','D35.2','D35.3','D35.4','D42.0','D42.9','D43.0',
--                        'D43.1','D43.2','D43.3','D44.3','D44.4','D44.5','D49.6')
--                        
--        ) DX_EXCL ON HA.HSP_ACCOUNT_ID = DX_EXCL.HSP_ACCOUNT_ID
    
 WHERE HA.LOC_ID = 100000 
   AND HA.ACCT_BILLSTS_HA_C NOT IN (99, 40) -- EXCLUDE COMBINED AND VOIDED ACCOUNTS  
  -- PATIENT AGE AT ADMISSION CANNOT BE 18 OR OLDER
   AND TRUNC(MONTHS_BETWEEN(TRUNC(COALESCE( HA.ADM_DATE_TIME, PE.HOSP_ADMSN_TIME, PEH.HOSP_ADMSN_TIME)),TRUNC(PAT.BIRTH_DATE))/12) < 18 
   AND TRUNC(COALESCE( PE.HOSP_DISCHRG_TIME, HA.DISCH_DATE_TIME, PE.HOSP_ADMSN_TIME, HA.ADM_DATE_TIME))
              BETWEEN '01-JAN-2023' AND '01-JAN-2024'    
    --AND DX_EXCL.HSP_ACCOUNT_ID IS NULL
--* DX CODES 
   AND EDG.REF_BILL_CODE IN ('G40.011','G40.019','G40.111','G40.119','G40.211','G40.219','G40.311','G40.319','G40.411','G40.419','G40.803','G40.804','G40.813','G40.814',
'G40.823','G40.824','G40.833','G40.834','G40.843 ','G40.844 ','G40.911','G40.919','G40.A11','G40.A19','G40.B11','G40.B19','G40.C11 ','G40.C19')
        
--* PROCEDURES
 
  AND (ICD.REF_BILL_CODE IN 
  --('00B00ZZ', '00B03ZZ', '00B04ZZ', '00B70ZZ', '00B73ZZ', '00B74ZZ', '00T73ZZ', '00T74ZZ', '00570ZZ', '00573ZZ',
  --                  '00574ZZ', '00800ZZ', '00803ZZ', '00804ZZ', '00870ZZ', '00873ZZ', '00874ZZ', '4A1002Z', '4A1004Z', '4A100BZ')
                    
                            ('00B00ZX','00B00ZZ','00B03ZX','00B03ZZ','00B04ZX','00B04ZZ','00B70ZZ','00B73ZZ','00B74ZZ','00T70ZZ','00T73ZZ','00T74ZZ')
                    
        OR CPT.CPT_CODE IN ('61541','61543')
 )
       
 ORDER BY 1  