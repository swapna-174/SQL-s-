/*          
H31a. How many unique patients  received temporal lobe epilepsy surgery including laser ablation (See code list- must have at least one diagnosis 
code and at least one procedure code.) in 2023? [If none, please enter 0.] Of those, how many achieved Engel Class 1-2 after 12 months? 
[Please exclude patients with brain tumors and vascular lesions.]
    
    ________ (1) Unique patients 2023 
    ________ (2) Number achieving Engel class 1-2 after 12 months 
    ________ (3) Percent achieving Engle class 1-2 after 12 months 
    
*/

SELECT DISTINCT --HA.HSP_ACCOUNT_ID,  
      PAT.PAT_MRN_ID "MRN", PAT.PAT_NAME 
     , TO_CHAR(TRUNC(COALESCE(PEH.HOSP_ADMSN_TIME, PE.HOSP_ADMSN_TIME, HA.ADM_DATE_TIME)),'MM/DD/YYYY')  "ADMIT DT"
     , TO_CHAR(TRUNC(COALESCE(PEH.HOSP_DISCH_TIME, PE.HOSP_DISCHRG_TIME, HA.DISCH_DATE_TIME )),'MM/DD/YYYY') "DISCH DT"  
     , TO_CHAR(PAT.BIRTH_DATE, 'MM/DD/YYYY') "PATIENT DOB"
     , TO_CHAR(PAT.DEATH_DATE, 'MM/DD/YYYY') "DEATH DT"
     , TRUNC(MONTHS_BETWEEN(TRUNC(COALESCE( HA.ADM_DATE_TIME, PE.HOSP_ADMSN_TIME, PEH.HOSP_ADMSN_TIME)),TRUNC(PAT.BIRTH_DATE))/12) "ADMIT AGE"    
   --  , EDG.REF_BILL_CODE "DX CODE" 
 ,ICD.REF_BILL_CODE "PROC CODE"
     , SER.PROV_NAME "PROVIDER"
     , Z.NAME "SPECIALTY" 
   ---  , AP.PROV_NAME "ATTENDING" , AP.NAME "ATND SPCLTY"
  -- , DX_EXCL.REF_BILL_CODE
  FROM CLARITY.HSP_ACCOUNT HA
  LEFT JOIN CLARITY.HSP_ACCT_SBO SBO ON SBO.HSP_ACCOUNT_ID = HA.HSP_ACCOUNT_ID AND SBO.SBO_HAR_TYPE_C = 0 -- MIXED ONLY
  LEFT JOIN CLARITY.PATIENT PAT ON PAT.PAT_ID = HA.PAT_ID
  LEFT JOIN CLARITY.PAT_ENC PE ON PE.PAT_ENC_CSN_ID = HA.PRIM_ENC_CSN_ID
  LEFT JOIN CLARITY.PAT_ENC_HSP PEH ON PEH.PAT_ENC_CSN_ID = HA.PRIM_ENC_CSN_ID
  LEFT JOIN CLARITY.HSP_ACCT_DX_LIST DX ON DX.HSP_ACCOUNT_ID = HA.HSP_ACCOUNT_ID
-- DIAGNOSIS  
  LEFT JOIN CLARITY.CLARITY_EDG EDG ON EDG.DX_ID = DX.DX_ID 
  LEFT JOIN CLARITY.ZC_ACCT_CLASS_HA AC ON AC.ACCT_CLASS_HA_C = HA.ACCT_CLASS_HA_C         

-- PROVIDER
   LEFT JOIN CLARITY.CLARITY_SER SER ON SER.PROV_ID = HA.ATTENDING_PROV_ID           
   LEFT JOIN CLARITY.CLARITY_SER_SPEC SPEC ON SPEC.PROV_ID = SER.PROV_ID
   LEFT JOIN CLARITY.ZC_SPECIALTY Z ON Z.SPECIALTY_C = SPEC.SPECIALTY_C       

 -- ICD9 PROCEDURES
   LEFT JOIN CLARITY.HSP_ACCT_PX_LIST PX ON PX.HSP_ACCOUNT_ID = HA.HSP_ACCOUNT_ID 
   LEFT JOIN CLARITY.CL_ICD_PX ICD ON ICD.ICD_PX_ID = PX.FINAL_ICD_PX_ID        
 -- CPT CODES
   LEFT JOIN CLARITY.HSP_ACCT_CPT_CODES CPT ON CPT.HSP_ACCOUNT_ID = HA.HSP_ACCOUNT_ID   
   LEFT JOIN CLARITY_EAP_OT                EOT ON CPT.CPT_CODE         = EOT.CPT_CODE   
   LEFT JOIN CLARITY_EAP                   EAP ON EOT.PROC_ID          = EAP.PROC_ID      

-- LEFT OUTER JOIN  -- DX EXCLUSIONS
--        (SELECT DX2.HSP_ACCOUNT_ID, EDG2.DX_ID, EDG2.REF_BILL_CODE
